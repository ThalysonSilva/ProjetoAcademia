    public void excluirAluno() {

        if (jTF_CodAluno.getText().equals("")) {  //  ESSE IF VAI SER PARA FAZER UMA VERIFICAÇÃO, SE TEM CODIGO OU NÃO..

            JOptionPane.showMessageDialog(this, "Digite o Código");

        } else { // SE TIVER CODIGO VAI FAZER A SEGUINTE OPERAÇÃO

            int codigo = Integer.parseInt(jTF_CodAluno.getText());  // ESSA VARIVEL VAI CARREGAR OS VALORES DO CAMPO CITADO.
            objExcluirAluno = new AlunoDAOExcluir(); // codigo para instanciar o objeto

            try {

                if (JOptionPane.showConfirmDialog(this, "Confirma a exclusão do Aluno?", "Confirmação",
                        JOptionPane.YES_NO_CANCEL_OPTION) == JOptionPane.YES_OPTION) { // codigo para da 3 opções ao usuario, sim, não e cancelar
                    // codigo a cima vai ser muito utilizado para perguntar algo ao usuario
                    if (objExcluirAluno.ExcluirAluno(codigo)) { // se a caixa for respondida com sim, ele vai retornar a mensagem a baixo e executar.
                        JOptionPane.showMessageDialog(this, "Excluido com sucesso !");
                        botaocancelaraluno();
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao excluir o registro");
                    }
                } else {

                    JOptionPane.showMessageDialog(this, "Operação Cancelada !");
                    botaocancelaraluno();

                }

            } catch (SQLException erro) {
                JOptionPane.showMessageDialog(this, "Erro: Exclusão Não Realizada" + erro.getMessage());
            }

        }

    } // FIM DO METODO EXCLUIR ALUNO

