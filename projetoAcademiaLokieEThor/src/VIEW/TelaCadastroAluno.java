    public void calculaMulta() throws ParseException {
        Calendar dataLimite = Calendar.getInstance();
        dataLimite.add(Calendar.DAY_OF_YEAR, -30);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date dataCadastro;
        dataCadastro = sdf.parse(jFTF_DatadoCadastro_Aluno.getText());

        if (dataLimite.getTime().before(dataCadastro)) {

            jFTF_Multa_Aluno.setText("0");
        } else {

            jFTF_Multa_Aluno.setText("10");
        }
        salvarAlunos();
    }// FIM DO VALIDAR CAMPO MULTA

