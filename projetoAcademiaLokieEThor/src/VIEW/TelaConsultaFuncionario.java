 private void listarAlunos(){
      
      try {
          
          FuncionarioDAOConsulta objFuncionarioConsulta = new FuncionarioDAOConsulta();
          DefaultTableModel model = (DefaultTableModel) jTableTCFuncionario.getModel();
          model.setNumRows(0);
          
          ArrayList<FuncionariosDTO> lista = objFuncionarioConsulta.PesquisarFuncionarios();
          
          for(int num=0; num<lista.size(); num++){
              model.addRow(new Object[]{
                  
                  lista.get(num).getId_funcionario(),
                  lista.get(num).getNome(),
                  lista.get(num).getCpf(),
                  lista.get(num).getDataadmissao(),
                  lista.get(num).getFuncao(),
                  lista.get(num).getTurno()
              });
              
          }
          
          
      } catch (Exception erro){
          JOptionPane.showMessageDialog(this, "Listar Funcionarios"+erro);
      }
      
  } // fim do metodo listar alunos


