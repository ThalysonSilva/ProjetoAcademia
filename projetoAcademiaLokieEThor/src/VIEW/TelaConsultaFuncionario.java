    private void Jbcf_CapturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbcf_CapturarActionPerformed
        
        
        
         String tipo= "";
         String escolha= jctipo.getSelectedItem().toString().trim();
         if(escolha.equals("Nome")){ // argumento selecionado vai filtrar e puxar as informaçações
             tipo= " "+"fun_nome";
         }
         if(escolha.equals("CPF")){
             tipo=" "+"fun_cpf";
         }
         if(escolha.equals("Id")){
             tipo=" "+"id_funcionario";
         }
         if(escolha.equals("Função")){
             tipo=" "+"fun_funcao";
         }
         if(escolha.equals("Data Admissão")){
             tipo=" "+"fun_dataadmissao";
         }
         if(escolha.equals("Turno")){
             tipo=" "+"fun_turno";
         }
        String arg = jtarg.getText();
        
        DefaultTableModel mp1 = (DefaultTableModel) jTableTCFuncionario.getModel();
        int l=mp1.getRowCount();
        
        if(l>0){
            while(l>0){
                ((DefaultTableModel) jTableTCFuncionario.getModel()).removeRow(l-1);
                l--;
            } // fim do while
        } // FIM DO IF
        
        try {
            
            ResultSet rs = FuncionarioDAOConsulta.carregaTabelFuncionario(tipo, arg);
            
            DefaultTableModel model = (DefaultTableModel) jTableTCFuncionario.getModel();
            
            
            while(rs.next()){
                String Coluna0=rs.getString("id_funcionario");
                String Coluna1=rs.getString("fun_nome").toString().trim();
                String Coluna2=rs.getString("fun_cpf").toString().trim();
                String Coluna3=rs.getString("fun_dataadmissao").toString().trim();
                String Coluna4=rs.getString("fun_funcao").toString().trim();
                String Coluna5=rs.getString("fun_turno").toString().trim();
                
              mp1.addRow(new String[] {Coluna0,Coluna1,Coluna2,Coluna3,Coluna4,Coluna5});
                
                
                
            }    
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro"+erro);
        }
        
        
        tamanhocolunas();
        jTableTCFuncionario.setAutoCreateRowSorter(true);
        
        
        
    }//GEN-LAST:event_Jbcf_CapturarActionPerformed

