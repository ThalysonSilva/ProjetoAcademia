    private void listarAlunos() {

        try {

            VisitantesDAOConsulta objVisitantesConsulta = new VisitantesDAOConsulta();
            DefaultTableModel model = (DefaultTableModel) jTableTCVisitantes.getModel();
            model.setNumRows(0);

            ArrayList<VisitantesDTO> lista = objVisitantesConsulta.PesquisarVisitantes();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId_visitante(),
                    lista.get(num).getNome(),
                    lista.get(num).getCpf(),
                    lista.get(num).getDatanascimento(),
                    lista.get(num).getTelefone(),
                    lista.get(num).getValordiaria()
                });

            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, "Listar Alunos" + erro);
        }

    } // fim do metodo listar alunos

