    private void Jbcv_CapturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbcv_CapturarActionPerformed
       
        
         String tipo= "";
         String escolha= jctipo.getSelectedItem().toString().trim();
         if(escolha.equals("Nome")){ // argumento selecionado vai filtrar e puxar as informaçações
             tipo= " "+"vis_nome";
         }
         if(escolha.equals("CPF")){
             tipo=" "+"vis_cpf";
         }
         if(escolha.equals("Id")){
             tipo=" "+"id_visitantes";
         }
         
        
         
        String arg = jtarg.getText();
        
        DefaultTableModel mp1 = (DefaultTableModel) jTableTCVisitantes.getModel();
        int l=mp1.getRowCount();
        
        if(l>0){
            while(l>0){
                ((DefaultTableModel) jTableTCVisitantes.getModel()).removeRow(l-1);
                l--;
            } // fim do while
        } // FIM DO IF
        
        try {
            
            ResultSet rs = VisitantesDAOConsulta.carregaTabelVisitantes(tipo, arg);
            
            DefaultTableModel model = (DefaultTableModel) jTableTCVisitantes.getModel();
            
            
            while(rs.next()){
                String Coluna0=rs.getString("id_visitantes");
                String Coluna1=rs.getString("vis_nome").toString().trim();
                String Coluna2=rs.getString("vis_cpf").toString().trim();
                String Coluna3=rs.getString("vis_datanascimento").toString().trim();
                String Coluna4=rs.getString("vis_telefone").toString().trim();
                String Coluna5=rs.getString("vis_valordiaria").toString().trim();
                
              mp1.addRow(new String[] {Coluna0,Coluna1,Coluna2,Coluna3,Coluna4,Coluna5});
                
                
                
            }    
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro"+erro);
        }
        
        
        tamanhocolunas();
        jTableTCVisitantes.setAutoCreateRowSorter(true);
        
        
        
        
        
        
    }//GEN-LAST:event_Jbcv_CapturarActionPerformed

